# Пример задачи:
# "Найти сумму чисел от 1 до N, где N вводит пользователь."

# Решение:
# python
n = int(input("Введите число N: "))
sum = 0
i = 1

while i <= n:
    sum += i
    i += 1

print(f"Сумма чисел от 1 до {n} равна {sum}")
# Объяснение:
# n = int(input("Введите число N: ")) – пользователь вводит число N.

# sum = 0 – переменная для хранения суммы.

# i = 1 – счетчик, начинаем с 1.

# while i <= n: – цикл выполняется, пока i не превысит N.

# sum += i – добавляем текущее значение i к сумме.

# i += 1 – увеличиваем счетчик на 1.

# После завершения цикла выводим результат.